export function getStoredLanguage() {
  if (typeof window === "undefined") {
    return "ru";
  }

  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return stored && translations[stored] ? stored : "ru";
}

export function setStoredLanguage(language) {
  if (typeof window === "undefined") {
    return;
  }

  const nextLanguage = translations[language] ? language : "ru";
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: nextLanguage }));
}

export function getText(language, scope, key) {
  const section = translations[language]?.[scope] ?? translations.ru[scope];
  return section?.[key] ?? translations.ru[scope]?.[key] ?? key;
}
